%% ====================================================================
%% API functions
%% ====================================================================
-export([cube/1,fac/1,mult/3]).

cube(X) ->     
	X * X * X. 

fac(1) ->    
	1; 

fac(N) ->     
	N * fac(N - 1).

mult(M,L,N) ->
     M*L*N.



